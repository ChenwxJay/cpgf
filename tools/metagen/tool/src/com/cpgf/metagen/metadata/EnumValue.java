package com.cpgf.metagen.metadata;

public class EnumValue {
	private String name;
	private String value;
	
	public EnumValue(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
	
}
